def calcule(num1,operator,num2):
    if operator == '+':
        print( num1 + num2 )
    elif operator == '-':
        print( num1 - num2 )
    elif operator == '*':
        print( num1 * num2 )
    elif operator == '/':
        print( num1 / num2 )
    

calcule(5,'+',4)
calcule(7,'*',2)
calcule(6,'/',3)
calcule(10,'-',5)